//! Broker — the transport-agnostic bridge between the core and the
//! language SDK's native `Client`.
//!
//! The core never opens sockets or spawns subprocesses. Instead, each
//! language binding implements [`McpConnection`] by delegating to the SDK's
//! own `Client.listTools()` / `Client.callTool()`. This keeps all transport
//! handling, auth, retries, and streaming inside the SDK that already does
//! it well, and lets the core focus on the augmentation logic.

use crate::types::{CallToolResult, Tool};
use crate::CoreError;
use async_trait::async_trait;
use parking_lot::RwLock;
use serde_json::Value;
use std::collections::HashMap;
use std::sync::Arc;

/// One MCP server connection, adapted onto the native SDK client.
#[async_trait]
pub trait McpConnection: Send + Sync {
    /// Unique, stable identifier. Usually the server name the user assigned.
    fn name(&self) -> &str;

    /// Fetch the live tool list from the server (equivalent to the SDK's
    /// `client.listTools()`).
    async fn list_tools(&self) -> Result<Vec<Tool>, CoreError>;

    /// Forward a tool call to the server (equivalent to `client.callTool()`).
    async fn call_tool(
        &self,
        name: &str,
        args: Option<Value>,
    ) -> Result<CallToolResult, CoreError>;
}

/// A collection of live MCP connections. In most cases a host has many
/// (e.g. `salesforce`, `github`, `filesystem`) and wants to treat them as
/// one tool catalog.
pub struct Broker {
    connections: RwLock<HashMap<String, Arc<dyn McpConnection>>>,
}

impl Broker {
    pub fn new() -> Self {
        Self {
            connections: RwLock::new(HashMap::new()),
        }
    }

    pub fn with(connections: Vec<Arc<dyn McpConnection>>) -> Self {
        let broker = Self::new();
        for c in connections {
            broker.add(c);
        }
        broker
    }

    pub fn add(&self, conn: Arc<dyn McpConnection>) {
        self.connections
            .write()
            .insert(conn.name().to_string(), conn);
    }

    pub fn remove(&self, name: &str) {
        self.connections.write().remove(name);
    }

    pub fn names(&self) -> Vec<String> {
        self.connections.read().keys().cloned().collect()
    }

    /// Aggregate tool list across all connected servers. Each tool's
    /// `server` field is populated so downstream code can route calls.
    pub async fn all_tools(&self) -> Result<Vec<Tool>, CoreError> {
        let conns: Vec<_> = self
            .connections
            .read()
            .values()
            .cloned()
            .collect();

        let mut out = Vec::new();
        for conn in conns {
            let tools = conn.list_tools().await?;
            let server = conn.name().to_string();
            for mut t in tools {
                t.server = Some(server.clone());
                out.push(t);
            }
        }
        Ok(out)
    }

    /// Call a tool by its *bare* name. If the name is ambiguous across
    /// servers, prefer a prefixed name (`server/tool`) as generated by
    /// [`crate::catalog::qualify`]. This mirrors goose's "extension__tool"
    /// convention in `extension_manager.rs::resolve_tool`.
    pub async fn call_tool(
        &self,
        name: &str,
        args: Option<Value>,
    ) -> Result<CallToolResult, CoreError> {
        // Qualified form: "server/tool"
        if let Some((server, tool)) = name.split_once('/') {
            let conn = {
                let guard = self.connections.read();
                guard.get(server).cloned()
            };
            match conn {
                Some(c) => return c.call_tool(tool, args).await,
                None => {
                    return Err(CoreError::Broker(format!(
                        "no connection named '{server}'"
                    )))
                }
            }
        }

        // Unqualified: find the unique connection that owns this tool.
        let conns: Vec<_> = self
            .connections
            .read()
            .values()
            .cloned()
            .collect();
        for conn in conns {
            // Cheap existence check: we only need to look at the tool list.
            // In a real impl we'd cache this; the stub fetches each time.
            if conn.list_tools().await?.iter().any(|t| t.name == name) {
                return conn.call_tool(name, args).await;
            }
        }
        Err(CoreError::Broker(format!("tool '{name}' not found")))
    }
}

impl Default for Broker {
    fn default() -> Self {
        Self::new()
    }
}
